package day16_ForLoopsStringsPractice;

public class OscarLive2 {
    public static void main(String[] args) {

        for (int i = 65; i <= 90; i++) {
            System.out.print((char) i + " ");

        }

        for (char c = 'A'; c <= 'Z'; c++) {
            System.out.print("c = " + c);

        }




    }
}