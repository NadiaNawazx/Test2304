package day05_Concatenation;

public class StudentInfo {
    public static void main(String[] args) {
/*
                1. student name (String)
    			2. age (int)
    			3. gender (char)
    			4. school name (String)
    			5. phone number (String) (###)-###-####
    			6. full time (boolean)
 */
        String StudentName = "Nadia";
        int age = 41;
        char gender = 70;
        String SchoolName = "Bristnall Hall High School";
        String PhoneNumber = "(0044) 7930 733307";
        boolean FullTime = true;
        double GPA = 2.5;

        System.out.println("StudentName = " + StudentName);
        System.out.println("age = " + age);
        System.out.println("gender = " + gender);
        System.out.println("SchoolName = " + SchoolName);
        System.out.println("PhoneNumber = " + PhoneNumber);
        System.out.println("FullTime = " + FullTime);
        System.out.println("GPA = " + GPA);




    }
}
