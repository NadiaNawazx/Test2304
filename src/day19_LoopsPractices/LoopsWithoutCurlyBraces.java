package day19_LoopsPractices;

public class LoopsWithoutCurlyBraces {

    public static void main(String[] args) {

        //without curly braces only one statement will be printed
        for (int i = 0; i < 5; i++)
            System.out.println("CYDEO");

        for (int i = 0; i < 5; i++)
            System.out.println("Batch 08");


    }
}
